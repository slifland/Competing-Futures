create table if not exists public.games (
  id text primary key,
  name text not null,
  round integer not null default 1 check (round >= 1),
  event_index integer not null default 0 check (event_index >= 0),
  status text not null default 'active',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.players (
  id text primary key,
  game_id text not null references public.games(id) on delete cascade,
  power_key text not null,
  name text not null,
  short_name text not null,
  accent text not null,
  role text not null,
  home_class text not null,
  capabilities integer not null check (capabilities between 1 and 10),
  safety integer not null check (safety between 1 and 10),
  market integer not null check (market between 1 and 10),
  support integer not null check (support between 1 and 10),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (game_id, power_key)
);

create table if not exists public.player_cards (
  id bigint generated by default as identity primary key,
  player_id text not null references public.players(id) on delete cascade,
  position integer not null,
  name text not null,
  text text not null,
  created_at timestamptz not null default now(),
  unique (player_id, position),
  unique (player_id, name)
);

create table if not exists public.events (
  id bigint generated by default as identity primary key,
  game_id text not null references public.games(id) on delete cascade,
  sort_order integer not null,
  title text not null,
  text text not null,
  created_at timestamptz not null default now(),
  unique (game_id, sort_order)
);

create table if not exists public.player_private_state (
  player_id text primary key references public.players(id) on delete cascade,
  objective text not null,
  selected_action text not null,
  updated_at timestamptz not null default now()
);

create or replace function public.set_updated_at()
returns trigger
language plpgsql
set search_path = public
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists games_set_updated_at on public.games;
create trigger games_set_updated_at
before update on public.games
for each row execute function public.set_updated_at();

drop trigger if exists players_set_updated_at on public.players;
create trigger players_set_updated_at
before update on public.players
for each row execute function public.set_updated_at();

