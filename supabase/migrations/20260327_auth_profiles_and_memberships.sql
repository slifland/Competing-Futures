create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  email text not null unique,
  full_name text,
  avatar_url text,
  app_role text not null default 'player' check (app_role in ('admin', 'player')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.games
  add column if not exists created_by uuid references auth.users(id) on delete set null,
  add column if not exists join_code text,
  add column if not exists completed_at timestamptz;

create unique index if not exists games_join_code_idx on public.games (join_code) where join_code is not null;

create table if not exists public.game_memberships (
  id bigint generated by default as identity primary key,
  game_id text not null references public.games(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  membership_role text not null default 'player' check (membership_role in ('player', 'observer')),
  power_key text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (game_id, user_id),
  check (
    (membership_role = 'player' and power_key is not null)
    or (membership_role = 'observer' and power_key is null)
  )
);

create unique index if not exists game_memberships_game_power_key_idx
on public.game_memberships (game_id, power_key)
where power_key is not null;

create table if not exists public.player_private_state (
  player_id text primary key references public.players(id) on delete cascade,
  objective text not null,
  selected_action text not null,
  updated_at timestamptz not null default now()
);

do $$
begin
  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'players'
      and column_name = 'objective'
  ) and exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'players'
      and column_name = 'selected_action'
  ) then
    insert into public.player_private_state (player_id, objective, selected_action)
    select id, objective, selected_action
    from public.players
    where objective is not null
    on conflict (player_id) do update
    set objective = excluded.objective,
        selected_action = excluded.selected_action,
        updated_at = now();
  end if;
end $$;

alter table public.players drop column if exists objective;
alter table public.players drop column if exists selected_action;

create or replace function public.generate_join_code()
returns text
language plpgsql
security definer
set search_path = ''
as $$
declare
  candidate text;
begin
  loop
    candidate := upper(substring(md5(random()::text || clock_timestamp()::text) from 1 for 6));
    exit when not exists (
      select 1
      from public.games
      where join_code = candidate
    );
  end loop;

  return candidate;
end;
$$;

update public.games
set join_code = public.generate_join_code()
where join_code is null;

