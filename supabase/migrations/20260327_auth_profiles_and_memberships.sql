create or replace function public.ensure_profile()
returns uuid
language plpgsql
security definer
set search_path = ''
as $$
declare
  current_user_id uuid := auth.uid();
  jwt_payload jsonb := auth.jwt();
  current_email text := lower(coalesce(jwt_payload ->> 'email', ''));
begin
  if current_user_id is null then
    raise exception 'Authentication required';
  end if;

  insert into public.profiles (id, email, full_name, avatar_url, app_role)
  values (
    current_user_id,
    current_email,
    coalesce(
      jwt_payload -> 'user_metadata' ->> 'full_name',
      jwt_payload -> 'user_metadata' ->> 'name'
    ),
    jwt_payload -> 'user_metadata' ->> 'avatar_url',
    case
      when current_email = 'sethlifland11@gmail.com' then 'admin'
      else 'player'
    end
  )
  on conflict (id) do update
  set email = excluded.email,
      full_name = coalesce(excluded.full_name, public.profiles.full_name),
      avatar_url = coalesce(excluded.avatar_url, public.profiles.avatar_url),
      app_role = case
        when excluded.email = 'sethlifland11@gmail.com' then 'admin'
        else public.profiles.app_role
      end,
      updated_at = now();

  return current_user_id;
end;
$$;

create or replace function public.is_admin()
returns boolean
language sql
stable
security definer
set search_path = ''
as $$
  select exists (
    select 1
    from public.profiles
    where id = auth.uid()
      and app_role = 'admin'
  );
$$;

create or replace function public.can_access_game(target_game_id text)
returns boolean
language sql
stable
security definer
set search_path = ''
as $$
  select
    auth.uid() is not null
    and (
      public.is_admin()
      or exists (
        select 1
        from public.game_memberships membership
        where membership.user_id = auth.uid()
          and membership.game_id = target_game_id
      )
      or exists (
        select 1
        from public.games game
        where game.id = target_game_id
          and game.created_by = auth.uid()
      )
    );
$$;

